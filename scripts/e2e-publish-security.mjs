// Browser proof for the literal generated-site boundary: source capability,
// anchor contextualization, URL policy, and CSP all survive actual export.
import { spawnSync } from "node:child_process";
import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { pathToFileURL, fileURLToPath } from "node:url";
import { chromium } from "playwright";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const TMP = fs.mkdtempSync(path.join(os.tmpdir(), "tine-publish-security-"));
for (const dir of ["pages", "journals", "logseq"]) fs.mkdirSync(path.join(TMP, dir), { recursive: true });
fs.writeFileSync(path.join(TMP, "pages", "Visible.md"), [
  "public:: true",
  "- Safe block [unsafe](javascript:globalThis.__tinePwned=1)",
  "  id:: quote\" space#/%",
  "- {{video javascript:globalThis.__tinePwned=2}}",
  "",
].join("\n"));
fs.writeFileSync(path.join(TMP, "pages", "Twin.md"), "public:: true\n- public twin\n");
fs.writeFileSync(path.join(TMP, "journals", "Twin.md"), "- PRIVATE TWIN SENTINEL\n");

const built = spawnSync("cargo", ["run", "-q", "-p", "tine-core", "--example", "publish_security_fixture", "--", TMP], {
  cwd: ROOT,
  encoding: "utf8",
  env: process.env,
});
if (built.status !== 0) throw new Error(`fixture export failed:\n${built.stderr}`);
const lines = built.stdout.trim().split(/\r?\n/);
if (lines[0] !== "1") throw new Error(`ambiguous public/private twin crossed capability boundary: ${built.stdout}`);
const output = lines.at(-1);
const htmlPath = path.join(output, "visible.html");
const html = fs.readFileSync(htmlPath, "utf8");
const search = fs.readFileSync(path.join(output, "search-index.js"), "utf8");
if (/href=["']javascript:/i.test(html) || html.includes(" onmouseover=") || search.includes("PRIVATE TWIN SENTINEL")) {
  throw new Error("generated bytes contain an unsafe URL/attribute or private-twin content");
}
if (!html.includes('id="quote&quot; space#/%"') || !search.includes('quote\\" space#/%')) {
  throw new Error("raw block identity was not safely preserved in its attribute/index contexts");
}
if (!fs.existsSync(path.join(output, "enhance.js")) || /\sonload=/.test(html)) {
  throw new Error("generated site still depends on inline script handlers");
}

const browser = await chromium.launch({ args: ["--no-sandbox", "--disable-gpu", "--disable-dev-shm-usage"] });
try {
  const page = await browser.newPage();
  await page.addInitScript(() => { globalThis.__tinePwned = 0; });
  await page.goto(pathToFileURL(htmlPath).href);
  const proof = await page.evaluate(() => ({
    pwned: globalThis.__tinePwned,
    unsafeHrefs: [...document.querySelectorAll("a")]
      .map((anchor) => anchor.getAttribute("href"))
      .filter((href) => href?.toLowerCase().startsWith("javascript:")),
    id: document.querySelector("main li")?.id,
    csp: document.querySelector('meta[http-equiv="Content-Security-Policy"]')?.getAttribute("content"),
  }));
  if (proof.pwned !== 0 || proof.unsafeHrefs.length || proof.id !== 'quote" space#/%'
      || !proof.csp?.includes("object-src 'none'") || !proof.csp?.includes("script-src 'self'")) {
    throw new Error(`browser publication boundary failed: ${JSON.stringify(proof)}`);
  }
  console.log("PASS: generated publication is a closed source/URL/anchor capability in Chromium");
} finally {
  await browser.close();
  fs.rmSync(TMP, { recursive: true, force: true });
}
